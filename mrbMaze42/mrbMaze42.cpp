void mrbMaze42::trigger(int power, int sensor, int step) {
	int a = sensor / 10;
	int b = sensor - (a * 10);
	while(1) {
		lineTraceSmooth(power);
		if (valSenDigi[a-1] == lineCol && valSenDigi[b-1] == lineCol) { break; }
	}
	motor(power,power,step);
}

