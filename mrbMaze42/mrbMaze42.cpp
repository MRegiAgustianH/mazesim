void mrbMaze42::motorDrive(int lSpeed, int rSpeed) {
	if (lSpeed >= 0) { 
		digitalWrite(lDir,HIGH); 
		analogWrite(lPwm,(lSpeed + 255) - (lSpeed * 2)); }
	else { 
		digitalWrite(lDir,LOW); 
		analogWrite(lPwm,(-lSpeed + 255) - (-lSpeed * 2)); }
	if (rSpeed >= 0) { 
		digitalWrite(rDir,LOW); 
		analogWrite(rPwm,(rSpeed + 255) - (rSpeed * 2)); }
	else { 
		digitalWrite(rDir,HIGH); 
		analogWrite(rPwm,(-rSpeed + 255) - (-rSpeed * 2)); }
}

